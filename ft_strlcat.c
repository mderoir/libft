#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	char	*d;
	char	*s;
	size_t	len;
	size_t	dst_len;

	if (!(d = (char *)ft_memchr(dst, '\0', dstsize)))
		return (dstsize + ft_strlen(src));
	s = (char *)src;
	d = (char *)dst;
	dst_len = ft_strlen(dst);
	len = dst_len + ft_strlen(s);
	d += dst_len;
	while (dst_len++ < dstsize - 1 && *s)
		*d++ = *s++;
	*d = '\0';
	return (len);
}
